void MapChip::Draw()
{
	int sizey = mapdata.size();
	for (int y = 0; y < sizey; y++)
	{
		int sizex = mapdata.at(y).size();
		for (int x = 0; x < sizex; x++)
		{
			MapData* data = &mapdata.at(y).at(x);

			if (data->key == "") continue;

			SpDS::DrawRotaGraph(x * tileSize, y * tileSize, 1.f, 1.f, 0.f, data->key, Anchor::TopLeft);
		}
	}
}
